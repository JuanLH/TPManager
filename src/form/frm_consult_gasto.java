    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // TODO add your handling code here:
        Gastos g = new Gastos();
        frm_dialog_motivo frm = new frm_dialog_motivo(this);
        try {
            int result = g.remove_gasto((int)tableGastos.getModel().getValueAt(tableGastos.getSelectedRow(), 0),frm.getMotivo());
            if(result ==1){
                Mensajes.mensajeInfo(evt, "SE ELIMINO CORRECTAMENTE");
                llenarTable();
            }
            else{
                Mensajes.mensajeInfo(evt, "NO SE ELIMINO");
            }
        } catch (SQLException ex) {
            Logger.getLogger(frm_consult_gasto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

