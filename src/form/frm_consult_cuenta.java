    public void llenar_txt(){
        DB dbase = Utilities.getConection();
        String cuenta_capital = "select monto from cuenta where id = 1";
        String cuenta_calle = "select monto from cuenta where id = 2";
        String cuenta_retirado = "select monto from cuenta where id = 3";
        try{
            ResultSet rs = dbase.execSelect(cuenta_capital);
            rs.next();
            txt_capital.setText(Integer.toString(rs.getInt(1)));
            
            rs = dbase.execSelect(cuenta_calle);
            rs.next();
            txt_calle.setText(Integer.toString(rs.getInt(1)));
            
            rs = dbase.execSelect(cuenta_retirado);
            rs.next();
            txt_retirado.setText(Integer.toString(rs.getInt(1)));
                   
                   
        
        }
        catch(SQLException e){
            System.out.println("error bdd --> "+e.getMessage());
        }
    }

