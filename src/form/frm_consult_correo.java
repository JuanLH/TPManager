    private void llenar_table(int id_cliente,java.awt.event.ActionEvent evt){
        Gson json = new Gson();
        ArrayList<Correo> lista = new ArrayList<Correo>();
        DefaultTableModel modelo = new DefaultTableModel();
        Correo correo = new Correo();
        String [] col = {"ID","ID_CLIENTE","EMAIL","ESTADO"};
        for (int i=0;i<col.length;i++)
            modelo.addColumn(col[i]);
        
        Respuesta respon = json.fromJson(correo.consultar_correo(id_cliente),Respuesta.class);
        
        
        if(respon.getId()>0){
            JsonElement son = new JsonParser().parse(respon.getMensaje());
            JsonArray array = son.getAsJsonArray();
            
            for(JsonElement json2 : array){
                Correo c  = json.fromJson(json2, Correo.class);
                lista.add(c);
            }
            
            int k;
            for(Correo email : lista){
                k=0;
                Object[] fila = new Object[4];
                fila[k++]=(Object)email.getId();
                fila[k++]=(Object)email.getCliente_id();
                fila[k++]=(Object)email.getCorreo();
                fila[k++]=(Object)email.getEstado();
                modelo.addRow(fila);
                
            }
            correo_table.setModel(modelo);
        }
        else if(respon.getId()==0){
            Mensajes.mensajeError(evt,"No se encontraron resultados");
        }
        else{
            Mensajes.mensajeError(evt,"Error de la base de datos");
            System.out.println("Error bdd :"+respon.getMensaje());
        }
            
    }
    
