    private void txt_cantKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_cantKeyTyped
        // TODO add your handling code here:
        char caract=evt.getKeyChar();

        int code = caract;
        if(code<48 || code>57) {

            getToolkit().beep();
            evt.consume();

        }
    }//GEN-LAST:event_txt_cantKeyTyped

    private void btn_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aceptarActionPerformed
        // TODO add your handling code here:
        if(Utilities.txtVoid(getContentPane())==false){
            Mensajes.mensajeError(evt, "TODOS LOS CAMPOS SON OBLIGATORIOS");
            return;
        }

        Respuesta respon = Cuenta.Retirar(Integer.parseInt(txt_cant.getText()),txt_descripcion.getText());
        if(respon.getId()>0){
            if(respon.getMensaje().equals("1")){
                Mensajes.mensajeInfo(evt, "SE RETIRO CON EXITO");
                this.dispose();
                
            }
            else if(respon.getMensaje().equals("0")){
                Mensajes.mensajeInfo(evt, "NO HAY FONDO SUFICIENTE PARA HACER ESTE RETIRO");
                Utilities.txtClean(this);
            }
            else{
                Mensajes.mensajeError(evt, "NO SE PUDO RETIRAR");
            }
        }

    }//GEN-LAST:event_btn_aceptarActionPerformed

