    private void btn_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aceptarActionPerformed
        // TODO add your handling code here:
        if(Utilities.txtVoid(getContentPane())==false){
            Mensajes.mensajeError(evt, "Todos los Campos son Obligatorios");
        }
        else{
            Abono abono = new Abono(Integer.parseInt(txt_id.getText()),Integer.parseInt(txt_id_prestamo.getText()),Integer.parseInt(txt_monto.getText()));
            String respon = abono.addAbono(abono);
            if(respon.equals("1")){
                txt_monto.setEnabled(false);
                lbl_buscar_prestamo.setVisible(false);
                Mensajes.mensajeInfo(evt, "SE AGREGO CORRECTAMENTE"); 
                btn_aceptar.setEnabled(false);
            }
            else{
                Mensajes.mensajeInfo(evt, "ERROR DE BASES DE DATOS");    
            }
        }
                
        
    }//GEN-LAST:event_btn_aceptarActionPerformed

